nbr1 = float(input("Entrez le premier nombre : "))
nbr2 = float(input("Entrez le deuxième nombre : "))
nbr3 = float(input("Entrez le troisième nombre : "))
if nbr1 <= nbr2 <= nbr3:
    print("Les nombres sont triés dans l'ordre croissant.")
else:
    print("Les nombres ne sont pas triés dans l'ordre croissant.")